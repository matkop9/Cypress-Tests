Cypress.Commands.add('setMobileViewPort', (device) => {
	if (device === 'mobile') {
		cy.viewport('iphone-6');
	} else if (device === 'tablet') {
		cy.viewport('ipad-2');
	} else {
		cy.viewport(1920, 1080);
	}
});
